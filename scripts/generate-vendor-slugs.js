import { readFileSync, writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');

const src = readFileSync(join(root, 'src/data/vendorProfiles.ts'), 'utf8');

// Extract keys of the form "category/vendor-slug": from the vendorProfiles record
const keys = [...src.matchAll(/^\s+"([a-z][a-z0-9]*\/[a-z0-9][a-z0-9-]*)"\s*:/gm)].map(m => m[1]);

writeFileSync(join(root, 'public/vendor-slugs.json'), JSON.stringify(keys, null, 2) + '\n');
console.log(`✓ Generated ${keys.length} vendor slugs → public/vendor-slugs.json`);

// Lightweight key index so main-chunk components (search, tables, signals) can do
// "does this vendor have a profile?" checks without importing the ~1MB profiles module.
const profileKeysTs = `/* AUTO-GENERATED by scripts/generate-vendor-slugs.js — do not edit by hand.
   Key index of vendorProfiles.ts ("category/vendor-slug"). Lets light components
   check profile existence without bundling the full profiles data module. */
export const profiledVendorKeys: ReadonlySet<string> = new Set(${JSON.stringify(keys, null, 2)});
`;
writeFileSync(join(root, 'src/data/profileKeys.ts'), profileKeysTs);
console.log(`✓ Generated ${keys.length} profile keys → src/data/profileKeys.ts`);
